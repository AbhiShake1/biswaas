<div class="container mx-auto max-w-3xl px-4 py-12">
  <h1 class="text-3xl font-bold">Review Guidelines</h1>
  <p class="mt-4 text-muted-foreground">
    Honest, helpful reviews are the foundation of Biswaas. Please follow these guidelines to keep our community trustworthy.
  </p>

  <div class="mt-8 space-y-8 text-muted-foreground">
    <section>
      <h2 class="text-xl font-semibold text-foreground">Be Honest & Authentic</h2>
      <p class="mt-2">Only review businesses you have genuinely interacted with. Share your real experience, whether positive or negative. Do not post reviews on behalf of others.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">Be Specific & Helpful</h2>
      <p class="mt-2">Include details about what you purchased or experienced, when it happened, and what stood out. Specific reviews help other consumers make better decisions.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">Be Respectful</h2>
      <p class="mt-2">Critique the service, not the person. Avoid personal attacks, discriminatory language, profanity, or threats. Focus on facts and your experience.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">No Conflicts of Interest</h2>
      <p class="mt-2">Do not review your own business or a competitor's business. Do not accept payment or incentives in exchange for reviews.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">No Spam or Fake Content</h2>
      <p class="mt-2">Do not post duplicate reviews, promotional content, or irrelevant material. Each review should be unique and relevant to the business.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">Protect Privacy</h2>
      <p class="mt-2">Do not include personal information about employees or other customers in your reviews, such as full names, phone numbers, or addresses.</p>
    </section>

    <section>
      <h2 class="text-xl font-semibold text-foreground">Reporting & Enforcement</h2>
      <p class="mt-2">If you see a review that violates these guidelines, please report it. Our team reviews flagged content and may remove reviews or suspend accounts that repeatedly break the rules.</p>
    </section>
  </div>
